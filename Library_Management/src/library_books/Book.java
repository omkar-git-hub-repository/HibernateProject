package library_books;

public abstract class Book {
	
	
	
	// Using abstract method set values 
	
	public abstract void setBookID(int bookid);
	public abstract void setBookName(String bookname);
	
	public abstract void setBookAuthor(String bookauthor);
	public abstract void setBookPublished(int publishedYear);
	
	public abstract void setBookPages(int bookpages);
	public abstract void setBookPrise(float bookprise);
	
	
	
}
